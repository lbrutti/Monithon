<h2>Cosa sono i cookies?</h2>

<p>I cookies sono file di testo sul tuo computer che raccolgono informazioni standard da Internet sulla tua navigazione e sui tuoi comportamenti online. Quando visiti il nostro sito web, potremmo raccogliere informazioni su di te in modo automatico attraverso cookies o simili tecnologie.</p>

<p>Per maggiori informazioni, visita questo sito: <a href="https://allaboutcookies.org">allaboutcookies.org</a>.</p>

<h2>Come usiamo i cookies?</h2>

<p>Monithon usa i cookies per mantenerti loggato una volta che hai effettuato l’accesso al nostro sito e per tracciare l’uso che fai del nostro sito al fine di migliorare la tua esperienza come utente.</p>

<h2>Quali tipi di cookies usiamo?</h2>

<p>Monithon usa le seguenti tipologie di cookies:</p>

<ul>
  <li><strong>Cookies di sessione:</strong> si tratta di cookies attivi per la sola sessione di riferimento che permettono di memorizzare temporaneamente le informazioni necessarie per la navigazione da una pagina all’altra evitando di doverle inserire nuovamente.</li>

<li><strong>Cookies di funzionalità:</strong> si tratta di cookies utilizzati per memorizzare personalizzazioni scelte dall'utente, quali, ad esempio, la lingua. In particolare questo il sito di Monithon fa uso dei cookies che non vengono memorizzati in modo persistente sul computer dell’utente, il cui impiego è strettamente limitato al corretto funzionamento di suddetta tecnologia, che altrimenti cesserebbe di funzionare. Questi cookies non registrano nessun tipo di informazione che possa ricondurre al singolo utente visitatore.</li>

<li><strong>Cookies analitici di terze parti:</strong> potranno essere installati cookies che permettono di calcolare il numero aggregato di visite del sito stesso e di identificare quali parti dello stesso sono più popolari. Questa operazione permette di ottenere un riscontro sull’utilizzo del sito e, conseguentemente, di intervenire adeguatamente sullo stesso per migliorarne la fruibilità da parte dei nostri utenti e clienti. Tale attività viene effettuata impiegando il servizio fornito dalla società Google denominato Google Analytics.</li>
</ul>
<h2>Come puoi gestire i cookies</h2>

<p>I cookies possono essere attivati e disattivati mediante il proprio browser. Accettando i cookies le informazioni saranno conservate e ripristinate nella navigazione successiva. È possibile bloccarli a livello globale o per alcuni siti specifici di cui non si vuole fornire e quindi conservare determinate informazioni. Tutti i browser moderni permettono di modificare le impostazioni sui cookies.</p>
